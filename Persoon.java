public class Persoon 
{
    private int BSN;
    private String voornaam;
    private String achternaam;
    private int geboortedag;
    private int geboortemaand;
    private int geboortejaar;
    private char geslacht;

public Persoon(String voornaam, String achternaam, int geboortedag, int geboortemaand, int geboortejaar, char geslacht) 
{
        BSN = 0;
        voornaam = voornaam;
        achternaam = achternaam;
        geslacht = geslacht;   
        geboortedag = 0; 
        geboortemaand = 0; 
        geboortejaar = 0; 
    
}
}            
        
            
            
       
         